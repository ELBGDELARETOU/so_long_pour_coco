#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	char		digi;
	long int	num;

	num = n;
	if (num < 0)
	{
		write(fd, "-", 1);
		num = -num;
	}
	if (num > 9)
		ft_putnbr_fd(num / 10, fd);
	digi = num % 10 + '0';
	write(fd, &digi, 1);
}
